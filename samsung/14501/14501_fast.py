'''
refactored by gemini
내 코드와 차이 : 딕셔너리 대신 리스트 사용, max 값을 최종 계산하지 않고, 한 번의 순회에서 계산 완료.
따라서 해당 날짜에 상담을 하는 것을 전제로 하지 않고, 상담을 하지 않는 경우에는 다음 날의 최대 수익을 저장 (오늘 상담하지 않음)
내가 생각한 이 코드의 한계 : 이 문제의 업그레이드 버전 - 특정 날짜에는 반드시 상담을 한다 라는 조건이 추가된 경우의 최대 수익을 구할 수 없음
'''

import sys

input = sys.stdin.readline

N = int(input())
# T(시간)와 P(금액)를 담을 리스트 (인덱스 편의를 위해 N+1 크기)
T = [0] * (N + 1)
P = [0] * (N + 1)

for i in range(1, N + 1):
    T[i], P[i] = map(int, input().split())

# dp[i]는 i번째 날부터 퇴사일까지 얻을 수 있는 최대 이익
# 뒤에서부터 계산하므로 N+2 크기로 잡아 인덱스 에러 방지
dp = [0] * (N + 2)

# 뒤에서부터 거꾸로 확인 (Bottom-Up)
for i in range(N, 0, -1):
    # 상담이 퇴사 전(N+1일 이전)에 끝나는 경우
    if i + T[i] <= N + 1:
        # 선택 1: 오늘 상담을 한다 -> 오늘 벌 돈 + 상담 끝난 다음 날의 최대 수익
        # 선택 2: 오늘 상담을 안 한다 -> 내일의 최대 수익(이미 계산됨)
        dp[i] = max(P[i] + dp[i + T[i]], dp[i + 1])
    else:
        # 오늘 상담을 못 하면 내일의 최대 수익을 그대로 가져옴
        dp[i] = dp[i + 1]

print(dp[1])